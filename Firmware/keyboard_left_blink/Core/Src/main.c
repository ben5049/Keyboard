/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
	PixelRGB_t pixel[NUM_PIXELS] = {0};
	uint32_t dmaBuffer[DMA_BUFF_SIZE] = {0};
	uint32_t *pBuff;
	int i, j, k;
	uint16_t stepSize;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_UCPD1_Init();
  MX_UCPD2_Init();
  /* USER CODE BEGIN 2 */
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	k = 0;
	stepSize = 4;
	while (1)
	{

		HAL_GPIO_TogglePin(STAT1_LED_GPIO_Port, STAT1_LED_Pin);
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		for (i = (NUM_PIXELS - 1); i > 0; i--)
		{
			pixel[i].data = pixel[i-1].data;
		}

		if (k < 255)
		{
			pixel[0].color.g = 254 - k; //[254, 0]
			pixel[0].color.r =  k + 1;  //[1, 255]
			pixel[0].color.b = 0;
		}
		else if (k < 510)
		{
			pixel[0].color.g = 0;
			pixel[0].color.r = 509 - k; //[254, 0]
			pixel[0].color.b = k - 254; //[1, 255]
			j++;
		}
		else if (k < 765)
		{
			pixel[0].color.g = k - 509; //[1, 255];
			pixel[0].color.r = 0;
			pixel[0].color.b = 764 - k; //[254, 0]
		}
		k = (k + stepSize) % 765;

		// not so bright
//		pixel[0].color.g >>= 2;
//		pixel[0].color.r >>= 2;
//		pixel[0].color.b >>= 2;
		pixel[0].color.g = 0;
		pixel[0].color.r = 0;
		pixel[0].color.b = 0;

		pBuff = dmaBuffer;
		for (i = 0; i < NUM_PIXELS; i++)
		{
			for (j = 23; j >= 0; j--)
			{
				if ((pixel[i].data >> j) & 0x01)
				{
					*pBuff = NEOPIXEL_ONE;
				}
				else
				{
					*pBuff = NEOPIXEL_ZERO;
				}
				pBuff++;
			}
		}
		dmaBuffer[DMA_BUFF_SIZE - 1] = 0; // last element must be 0!

		HAL_TIM_PWM_Start_DMA(&htim4, TIM_CHANNEL_2, dmaBuffer, DMA_BUFF_SIZE);

		HAL_Delay(10);
	}
  /* USER CODE END 3 */
}

